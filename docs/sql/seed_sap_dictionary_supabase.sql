INSERT INTO sap_dictionary_embeddings (
    source_key,
    source_type,
    table_name,
    field_name,
    module,
    domain_name,
    description,
    content_text,
    embedding
)
SELECT
    CONCAT('table:', t.name) AS source_key,
    'table' AS source_type,
    t.name AS table_name,
    NULL AS field_name,
    t.module,
    d.name AS domain_name,
    t.description,
    CONCAT(
        'Tabela SAP ', t.name,
        ' do modulo ', t.module,
        '. Dominio: ', d.name,
        '. Descricao: ', t.description
    ) AS content_text,
    NULL AS embedding
FROM sap_tables t
JOIN sap_domains d ON d.id = t.domain_id
ON CONFLICT (source_key) DO UPDATE
SET
    source_type = EXCLUDED.source_type,
    table_name = EXCLUDED.table_name,
    field_name = EXCLUDED.field_name,
    module = EXCLUDED.module,
    domain_name = EXCLUDED.domain_name,
    description = EXCLUDED.description,
    content_text = EXCLUDED.content_text,
    embedding = CASE
        WHEN sap_dictionary_embeddings.content_text IS DISTINCT FROM EXCLUDED.content_text
            THEN NULL
        ELSE sap_dictionary_embeddings.embedding
    END;

