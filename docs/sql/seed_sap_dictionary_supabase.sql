        WHEN sap_dictionary_embeddings.content_text IS DISTINCT FROM EXCLUDED.content_text
            THEN NULL
        ELSE sap_dictionary_embeddings.embedding
    END;

CREATE INDEX IF NOT EXISTS idx_sap_embeddings_vector_cosine
    ON sap_dictionary_embeddings
    USING ivfflat (embedding vector_cosine_ops)
    WITH (lists = 10);

CREATE OR REPLACE FUNCTION match_sap_dictionary_embeddings(
    query_embedding VECTOR(384),
    match_count INTEGER DEFAULT 10,
    filter_source_type TEXT DEFAULT NULL,
    filter_domain_name TEXT DEFAULT NULL,
    filter_module TEXT DEFAULT NULL
)
RETURNS TABLE (
    source_key TEXT,
    source_type TEXT,
    table_name TEXT,
    field_name TEXT,
    module TEXT,
    domain_name TEXT,
    description TEXT,
    content_text TEXT,
    similarity DOUBLE PRECISION
)
LANGUAGE SQL
AS $$
    SELECT
        e.source_key,
        e.source_type,
        e.table_name,
        e.field_name,
        e.module,
        e.domain_name,
        e.description,
        e.content_text,
        1 - (e.embedding <=> query_embedding) AS similarity
    FROM sap_dictionary_embeddings e
    WHERE e.embedding IS NOT NULL
      AND (filter_source_type IS NULL OR e.source_type = filter_source_type)
      AND (filter_domain_name IS NULL OR e.domain_name = filter_domain_name)
      AND (filter_module IS NULL OR e.module = filter_module)
    ORDER BY e.embedding <=> query_embedding
    LIMIT match_count;
$$;

