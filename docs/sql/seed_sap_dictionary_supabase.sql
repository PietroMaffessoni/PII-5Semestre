CREATE TABLE IF NOT EXISTS sap_domains (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL UNIQUE,
    chart_recommendation TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS sap_domain_terms (
    domain_id BIGINT NOT NULL REFERENCES sap_domains(id) ON DELETE CASCADE,
    term TEXT NOT NULL,
    PRIMARY KEY (domain_id, term)
);

CREATE TABLE IF NOT EXISTS sap_tables (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL UNIQUE,
    module TEXT NOT NULL,
    domain_id BIGINT NOT NULL REFERENCES sap_domains(id),
    description TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS sap_table_terms (
    table_id BIGINT NOT NULL REFERENCES sap_tables(id) ON DELETE CASCADE,
    term TEXT NOT NULL,
    PRIMARY KEY (table_id, term)
);

CREATE TABLE IF NOT EXISTS sap_table_join_keys (
    table_id BIGINT NOT NULL REFERENCES sap_tables(id) ON DELETE CASCADE,
    join_key TEXT NOT NULL,
    PRIMARY KEY (table_id, join_key)
);

CREATE TABLE IF NOT EXISTS sap_fields (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    table_id BIGINT NOT NULL REFERENCES sap_tables(id) ON DELETE CASCADE,
    field_name TEXT NOT NULL,
    label TEXT NOT NULL,
    data_type TEXT NOT NULL,
    description TEXT NOT NULL,
    UNIQUE (table_id, field_name)
);

CREATE TABLE IF NOT EXISTS sap_dictionary_embeddings (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    source_key TEXT NOT NULL UNIQUE,
    source_type TEXT NOT NULL CHECK (source_type IN ('table', 'field')),
    table_name TEXT NOT NULL,
    field_name TEXT,
    module TEXT NOT NULL,
    domain_name TEXT NOT NULL,
    description TEXT NOT NULL,
    content_text TEXT NOT NULL,
    embedding VECTOR(384)
);

ALTER TABLE sap_dictionary_embeddings
    ADD COLUMN IF NOT EXISTS source_key TEXT;

UPDATE sap_dictionary_embeddings
SET source_key = CASE
    WHEN source_type = 'table' THEN CONCAT('table:', table_name)
    ELSE CONCAT('field:', table_name, '.', field_name)
END
WHERE source_key IS NULL;

ALTER TABLE sap_dictionary_embeddings
    ALTER COLUMN source_key SET NOT NULL;

