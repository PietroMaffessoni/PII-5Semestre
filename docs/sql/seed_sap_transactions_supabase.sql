CREATE TABLE IF NOT EXISTS afko (
    aufnr TEXT PRIMARY KEY,
    werks TEXT NOT NULL,
    gstrp DATE NOT NULL,
    gltrp DATE NOT NULL,
    gamng NUMERIC(14, 2) NOT NULL
);

CREATE TABLE IF NOT EXISTS afpo (
    aufnr TEXT NOT NULL,
    matnr TEXT NOT NULL,
    psmng NUMERIC(14, 2) NOT NULL,
    PRIMARY KEY (aufnr, matnr)
);

CREATE TABLE IF NOT EXISTS vbrk (
    vbeln TEXT PRIMARY KEY,
    fkdat DATE NOT NULL,
    kunnr TEXT NOT NULL,
    vkorg TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS vbrp (
    vbeln TEXT NOT NULL,
    matnr TEXT NOT NULL,
    fkimg NUMERIC(14, 2) NOT NULL,
    netwr NUMERIC(14, 2) NOT NULL,
    PRIMARY KEY (vbeln, matnr)
);

CREATE TABLE IF NOT EXISTS ekko (
    ebeln TEXT PRIMARY KEY,
    bedat DATE NOT NULL,
    lifnr TEXT NOT NULL,
    ekgrp TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS ekpo (
    ebeln TEXT NOT NULL,
    matnr TEXT NOT NULL,
    menge NUMERIC(14, 2) NOT NULL,
    netwr NUMERIC(14, 2) NOT NULL,
    PRIMARY KEY (ebeln, matnr)
);

CREATE TABLE IF NOT EXISTS script_query_history (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT,
    requested_by TEXT NOT NULL,
    question TEXT NOT NULL,
    generated_sql TEXT NOT NULL,
    retrieval_mode TEXT NOT NULL,
    execution_status TEXT NOT NULL,
    row_count INTEGER NOT NULL DEFAULT 0,
    result_preview JSONB NOT NULL DEFAULT '[]'::jsonb,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_script_query_history_user_id
    ON script_query_history (user_id, created_at DESC);

CREATE INDEX IF NOT EXISTS idx_script_query_history_requested_by
    ON script_query_history (requested_by, created_at DESC);

